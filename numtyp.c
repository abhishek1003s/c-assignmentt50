#include <stdio.h>

int main() {
    int intVar = 42;
    float floatVar = 3.14;
    double doubleVar = 3.14983;
    char charVar = 'A';

    printf("Integer: %d\n", intVar);
    printf("Float: %f\n", floatVar);
    printf("Double: %lf\n", doubleVar);
    printf("Character: %c\n", charVar);

    return 0;
}
